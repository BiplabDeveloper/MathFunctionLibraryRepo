package com.example.mathfunctionlibrary

public class CommonMathFunction {
    public fun addition(a: Int, b: Int): Int {
        return a + b
    }

    public fun subtraction(a: Int, b: Int): Int {
        return a - b
    }

    public fun multiplication(a: Int, b: Int): Int {
        return a * b
    }

    public fun division(a: Int, b: Int): Int {
        return a / b
    }
}